public class RegistrationAdminDTO {
	
	private String seq;
	private String regDate;
	private String regState;
	private String completionDate;
	private String failDate;
	private String studentSeq;
	private String openCourSeq;
	private String date;
	

	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getRegState() {
		return regState;
	}
	public void setRegState(String regState) {
		this.regState = regState;
	}
	public String getCompletionDate() {
		return completionDate;
	}
	public void setCompletionDate(String completionDate) {
		this.completionDate = completionDate;
	}
	public String getFailDate() {
		return failDate;
	}
	public void setFailDate(String failDate) {
		this.failDate = failDate;
	}
	public String getStudentSeq() {
		return studentSeq;
	}
	public void setStudentSeq(String studentSeq) {
		this.studentSeq = studentSeq;
	}
	public String getOpenCourSeq() {
		return openCourSeq;
	}
	public void setOpenCourSeq(String openCourSeq) {
		this.openCourSeq = openCourSeq;
	}
	
	


}
